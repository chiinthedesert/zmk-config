// 2. THE IMAGE DESCRIPTOR
static const lv_img_dsc_t my_art_desc = {
    .header.always_zero = 0,
    .header.w = 32,
    .header.h = 128,
    .data_size = sizeof(my_art_bits),
    .header.cf = LV_IMG_CF_INDEXED_1BIT, // This is key for 1-bit OLEDs
    .data = my_art_bits,
};

// State storage for the Left-side widget
static struct zmk_widget_status status_widget;

lv_obj_t *zmk_display_status_screen() {
    lv_obj_t *screen = lv_obj_create(NULL);

    if (zmk_split_is_central()) {
        // --- LEFT SIDE: BUILT-IN DASHBOARD ---
        zmk_widget_status_init(&status_widget, screen);
        lv_obj_align(zmk_widget_status_obj(&status_widget), LV_ALIGN_TOP_LEFT, 0, 0);
    } else {
        // --- RIGHT SIDE: YOUR PIXEL ART ---
        lv_obj_t *img = lv_img_create(screen);
        lv_img_set_src(img, &my_art_desc);
        lv_obj_align(img, LV_ALIGN_CENTER, 0, 0);
    }

    return screen;
}
